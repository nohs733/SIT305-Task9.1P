package com.example.task91p;

public class Model {

    String placename, placelocation, id;

    public Model(String id, String title, String description) {
        this.placename = placename;
        this.placelocation = placelocation;
        this.id = id;
    }

    public String getplacename() {
        return placename;
    }

    public void setplacename(String title) {
        this.placename = placename;
    }

    public String getplacelocation() {
        return placelocation;
    }

    public void setplacelocation(String description) {
        this.placelocation = description;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

}
